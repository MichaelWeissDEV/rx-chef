use crate::operation::{ArgSchema, ArgValue, DataType, Operation, OperationError};

/// To HTML Entity operation
pub struct ToHTMLEntity;

impl Operation for ToHTMLEntity {
    fn name(&self) -> &'static str {
        "To HTML Entity"
    }

    fn module(&self) -> &'static str {
        "Encodings"
    }

    fn description(&self) -> &'static str {
        "Converts characters to HTML entities<br><br>e.g. <code>&amp;</code> becomes <code>&amp;<span>amp;</span></code>"
    }

    fn args_schema(&self) -> &'static [ArgSchema] {
        static SCHEMA: &[ArgSchema] = &[
            ArgSchema {
                name: "Convert all characters",
                description: "Convert all characters to HTML entities",
                default_value: "false",
            },
            ArgSchema {
                name: "Convert to",
                description: "The type of HTML entity to convert to",
                default_value: "Named entities",
            },
        ];
        SCHEMA
    }

    fn input_type(&self) -> DataType {
        DataType::String
    }

    fn output_type(&self) -> DataType {
        DataType::String
    }

    fn run(&self, input: Vec<u8>, args: &[ArgValue]) -> Result<Vec<u8>, OperationError> {
        let input_str = String::from_utf8_lossy(&input);

        let convert_all = args.first().and_then(|v| v.as_bool()).unwrap_or(false);
        let convert_to = args
            .get(1)
            .and_then(|v| v.as_str())
            .unwrap_or("Named entities");

        let numeric = convert_to == "Numeric entities";
        let hexa = convert_to == "Hex entities";

        let mut output = String::with_capacity(input_str.len() * 4);

        for c in input_str.chars() {
            let code = c as u32;

            if convert_all {
                if numeric {
                    output.push_str(&format!("&#{};", code));
                } else if hexa {
                    output.push_str(&format!("&#x{:x};", code));
                } else {
                    if let Some(entity) = get_named_entity(code) {
                        output.push_str(entity);
                    } else {
                        output.push_str(&format!("&#{};", code));
                    }
                }
            } else {
                if numeric {
                    if code > 255 || is_special_entity(code) {
                        output.push_str(&format!("&#{};", code));
                    } else {
                        output.push(c);
                    }
                } else if hexa {
                    if code > 255 || is_special_entity(code) {
                        output.push_str(&format!("&#x{:x};", code));
                    } else {
                        output.push(c);
                    }
                } else {
                    if let Some(entity) = get_named_entity(code) {
                        output.push_str(entity);
                    } else if code > 255 {
                        output.push_str(&format!("&#{};", code));
                    } else {
                        output.push(c);
                    }
                }
            }
        }

        Ok(output.into_bytes())
    }
}

