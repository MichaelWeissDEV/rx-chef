// Tests for the render_image operation.
// Run only these tests:
//   cargo test -p cyberchef-rust-tests --test operations render_image::

use rxchef::operation::ArgValue;
use rxchef::operations::render_image::RenderImageOp;
use rxchef::Operation;

#[test]
fn test_render_image_png() {
    let op = RenderImageOp;
    // A minimal 1x1 black PNG
    let input = vec![
        0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44,
        0x52, 0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x08, 0x00, 0x00, 0x00, 0x00, 0x3A,
        0x7E, 0x9B, 0x55, 0x00, 0x00, 0x00, 0x0A, 0x49, 0x44, 0x41, 0x54, 0x08, 0xD7, 0x63, 0x60,
        0x00, 0x00, 0x00, 0x02, 0x00, 0x01, 0xE2, 0x21, 0xBC, 0x33, 0x00, 0x00, 0x00, 0x00, 0x49,
        0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82,
    ];
    let args = [ArgValue::Str("Raw".to_string())];
    let result = op.run(input, &args).unwrap();
    let result_str = String::from_utf8(result).unwrap();
    assert!(result_str.starts_with("<img src='data:image/png;base64,"));
}
#[test]
fn test_empty_input() {
    let op = RenderImageOp;
    let result = op.run(vec![], &[ArgValue::Str("Raw".to_string())]).unwrap();
    assert_eq!(result, Vec::<u8>::new());
}
